   public especificacoes(usuario u )
   {
       if (u.getTipo().equalsIgnoreCase("paciente"))
       {
           responsavelcadtext.setEnabled(false);
           nomeresptext.setEnabled(false);
           nomeresptextfield.setEnabled(false);
           paciresptextfield.setEnabled(false);
           nomepesqtextfield.setEnabled(false);
           cadarespbutton.setEnabled(false);
       }
       else if(u.getTipo().equalsIgnoreCase("responsavel"))
       {
           responsavelcadtext.setEnabled(true);
           nomeresptext.setEnabled(true);
           nomeresptextfield.setEnabled(true);
           paciresptextfield.setEnabled(true);
           nomepesqtextfield.setEnabled(true);
           cadarespbutton.setEnabled(true);
       }
   }

