    private void pacicadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pacicadastrarActionPerformed
        // TODO add your handling code here:
        paciente pa = new paciente();
        pa.setNome(nometextfield.getText());
        pa.setIdade(idadetextfield.getText());
        pa.setCondicao(condicaotextfield.getText());
        pa.setRemedio(remediotextfield.getText());
        pa.setInfremedio(inftextarea.getText());
        
        pacienteJPA.cadastrar(pa);
        
    }//GEN-LAST:event_pacicadastrarActionPerformed

    private void cadarespbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadarespbuttonActionPerformed
        // TODO add your handling code here:
        paciente pa = new paciente();
        responsavel res = new responsavel();
        pacienteJPA paj = new pacienteJPA();
        
         String pegarnome = nomepesqtextfield.getText();
         
         int idpaciente = paj.buscarIdPorNome(pegarnome);
         
         
         res.setNome(nomeresptextfield.getText());
         res.setIdpaciente(idpaciente);
         
         responsavelJPA.cadastrar(res);
         
         
         

        
        
    }//GEN-LAST:event_cadarespbuttonActionPerformed

